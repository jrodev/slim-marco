<div id="contenido">
  <ul>
    <li><strong>DEFINICIONES OPERACIONALES</strong></li>
  </ul>
  <p><strong>EQUIPAMIENTO</strong></p>
  <ul>
    <li>EQUIPAMIENTO. Es el conjunto de bienes de un establecimiento de salud necesarios  para el desarrollo de prestaciones de salud o actividades administrativas.  Comprende equipos, mobiliario, instrumental y vehículos.</li>
  </ul>
  <ul>
    <li>EQUIPO MÉDICO.
    Es el equipo que se usa  con fines diagnósticos o tratamiento de enfermedades o de rehabilitación  después de una enfermedad o lesión; se les puede usar individualmente con  cualquier accesorio o consumible, o con otro equipo médico. Requieren  calibración y mantenimiento, actividades que deben ser realizadas por  ingenieros o técnicos de la especialidad.</li>
</ul>
  <ul>
    <li>EQUIPO BIOMÉDICO. Es el dispositivo médico  operacional que reúne sistemas y subsistemas eléctricos, electrónicos,  hidráulicos y/o híbridos, incluidos los programas informáticos que intervengan  en su buen funcionamiento, destinado por el fabricante a ser usado en seres  humanos con fines de prevención, diagnóstico, tratamiento o rehabilitación. No  constituye equipo biomédico, aquellos dispositivos médicos implantados en el  ser humano o aquellos destinados para un solo uso.   </li>
  </ul>
  <ul>
    <li>EQUIPO ELECTROMECÁNICO. Es el equipo que  combina partes eléctricas y mecánicas, y cuyo uso permite el adecuado  funcionamiento de los servicios asistenciales y hoteleros. En este rubro están  los equipos de los servicios de casa de fuerza, central de esterilización,  lavandería, nutrición cocina, manejo de residuos sólidos, aire acondicionado,  gases medicinales, refrigeración, equipos sistema contra incendios,  mantenimiento, seguridad, grupo electrógeno, ascensores, calderos, calentadores  de agua, bombas de agua o petróleo, ablandadores de agua, entre otros.</li>
  </ul>
  <ul>
    <li>EQUIPO INFORMÁTICO. Es el equipo que  permite el adecuado procesamiento de la información, mediante el uso de  software y/o programas. En este rubro se consideran las computadoras  personales, impresores, fotocopiadoras, proyectores multimedia y sistema de  gestión de imágenes médicas (PCAS/RIS), sistema de registro de historias  clínicas sistemas de gestión administrativa y logística, entre otros.</li>
  </ul>
  <ul>
    <li>MOBILIARIO. Constituido por todo  mueble de apoyo utilizado para efectuar una labor clínica o administrativa.  Ejemplo: mesa diván, mesa de curaciones, cama de hospitalización, camillas de  transporte, escritorios, sillas, archivadores, credenzas.</li>
  </ul>
  <ul>
    <li>INSTRUMENTAL. Es el conjunto de  instrumentos (set) usados durante la actividad asistencial. Ejemplo: Set  instrumental para apendicetomía, set instrumental para legrado uterino, entre  otros. </li>
  </ul>
  <ul>
    <li>EQUIPO ESTRATÉGICO. Equipos que permiten mejorar la calidad de los servicios, son  utilizados para la evaluación y examen de los pacientes brindando resultados  inmediatos y un buen tratamiento. </li>
  </ul>
  <ul>
    <li>EQUIPO DE ALTA TECNOLOGÍA. Modernos  sistemas electrónicos y equipos de alta tecnología médica con finalidad  diagnóstica y fines terapéuticos, cuyo costo de adquisición es de alta  inversión.</li>
  </ul>
  <p><strong>ESTADO DE CONSERVACIÓN DEL EQUIPAMIENTO</strong></p>
  <ul>
    <li>ESTADO DE CONSERVACIÓN. Factor que determina la necesidad de aplicación de las actividades  de reparación o reposición del equipo. Para determinar el estado de  conservación se deberá contar con el catálogo o manual del equipo. La  clasificación es la siguiente:</li>
  </ul>
  <ul>
    <li>BUENO. Equipo que se encuentra  operativo, rinde según estándar o especificación de fábrica dentro de sus  parámetros técnicos y físicos, se encuentra dentro de su tiempo de vida útil y  cuenta con mantenimiento preventivo. </li>
  </ul>
  <ul>
    <li>REGULAR O DEFICIENTE. Equipo que se encuentra  operativo, rinde alrededor del 50% de su estándar o especificación de fábrica  dentro de sus parámetros técnicos y físicos, ha superado su tiempo de vida útil  y no cuenta con mantenimiento preventivo.</li>
  </ul>
  <ul>
    <li>MALO. Equipo que no se  encuentra operativo; o está operativo y es obsoleto tecnológicamente, o  presenta defectos técnicos y/o físicos severos, ha superado su tiempo de vida  útil y su mantenimiento correctivo es superior al 40% del valor de adquisición  de uno nuevo.  </li>
  </ul>
  <p><strong>EQUIPOS SEGÚN NIVEL DE RIESGO</strong><br>
    Permite  establecer la jerarquía o prioridad de procesos, facilitando la toma de  decisiones, direccionando el esfuerzo y los recursos a aquellos equipos que  están relacionados con la protección y mantenimiento de la vida del paciente. </p>
  <ul>
    <li>EQUIPOS DE ALTO RIESGO: Son los equipos sujetos a controles especiales. Destinados a  proteger o mantener la vida del paciente o para un uso de importancia  sustancial en la prevención del deterioro de la salud humana. </li>
  </ul>
  <ul>
    <li>EQUIPOS DE MEDIANO RIESGO: Son los equipos sujetos a controles específicos. Destinados al  tratamiento de los pacientes para recuperar  su salud.</li></ul>
  <ul>
    <li>EQUIPOS DE BAJO RIESGO: Son los  equipos sujetos a controles generales. Destinados al complemento del  tratamiento de los pacientes.<strong></strong></li>
  </ul>
  <p><strong>INFRAESTRUCTURA.</strong></p>
  <ul>
    <li>INFRAESTRUCTURA. La infraestructura como el conjunto organizado de elementos  estructurales, no estructurales y equipamiento de obra de una edificación que  permite el desarrollo de prestaciones y actividades de salud.</li>
  </ul>
  <ul>
    <li>COMPONENTE DE LA  INFRAESTRUCTURA. ARQUITECTURA. Arte o técnica de  proyectar o construir edificios, según reglas, técnicas, cánones estéticos  determinados, considerándose para ello un conjunto de actividades para lograr  ambientes estéticos y confortables.</li>
  </ul>
  <ul>
    <li>ESTRUCTURA. Es un Conjunto de  elementos simples capaces de aguantar pesos y cargas sin romperse y sin apenas  deformarse.  Los cuales están  constituidos, generalmente, por hormigón, acero y concreto. Las cuales se  comportan como un esqueleto de enorme resistencia, capaz de trasladar el peso  de cada piso a los cimientos. . Es decir, es el esqueleto del Establecimiento  de Salud.</li>
  </ul>
  <blockquote>
    <p>En función de la  posición que ocupan y el esfuerzo que han de soportar, los elementos simples se  pueden clasificar en: cimentación corrida armada, Losas o Plateas, Zapatas  (zapatas aisladas, Zapatas combinadas, Zapatas conectadas), Vigas, Columnas,  Placas, Pilares, Pilotes.</p>
  </blockquote>
  <ul>
    <li>INSTALACIONES. Conjunto de redes, las  cuales están comprendidas las líneas vitales, que cumplen la función de  transportar energía, fluidos y datos desde un punto de generación hasta el  paciente, equipo o sistema que los utiliza. En los establecimientos de salud se  utilizan instalaciones de diversos tipos como:</li>
  </ul>
  <ul>
    <li>INSTALACIONES ELÉCTRICAS:
      <blockquote>
        <p>• Instalaciones  eléctricas de media tensión<br>
          •Instalaciones  eléctricas de baja tensión<br>
          •Instalaciones de protección  eléctrica: Pozos de tierra, pararrayos, etc.</p>
        </blockquote>
      </li>

      <li>INSTALACIONES MECÁNICAS:
          <blockquote>
            <p>• Instalaciones de  vapor<br>
              • Instalaciones de  gases medicinales<br>
              • Instalaciones de  vacío<br>
              • Instalaciones de aire  comprimido<br>
              • Instalaciones de aire  acondicionado</p>
          </blockquote>
      </li>

    <li>INSTALACIONES SANITARIAS:
      <blockquote>
        <p>• Instalaciones de  agua: potable, caliente, blanda.<br>
          • Instalaciones de  desagüe<br>
          • Instalaciones de agua  contraincendios</p>
        </blockquote>
      </li>

    <li>INSTALACIONES DE  TELECOMUNICACIONES:
      <blockquote>
        <p>• Instalaciones de  telefonía<br>
          • Instalaciones de  televisión y radiodifusión<br>
          • Instalaciones de  telecomunicaciones por cable</p>
      </blockquote>
      </li>

    <li>INFRAESTRUCTURA ESTRATÉGICA.
                            Edificación con la infraestructura e instalaciones acabadas,  comprende las siguientes Unidades Productoras de Servicios de Salud (UPSS):  Emergencia, Centro Obstétrico, Diagnóstico por Imagen, Centro Quirúrgico,  Cuidados Intensivos, Hospitalización, Patología Clínica y Farmacia.    </li></ul>
  <p><strong>MANTENIMIENTO</strong></p>
  <ul>
    <li>MANTENIMIENTO DE  INFRAESTRUCTURA Y  EQUIPAMIENTO. Son todas aquellas acciones que permitan mejorar las condiciones  existentes y mantener la operatividad de las instalaciones físicas de los  establecimientos de salud y del equipamiento existente sin ampliar o modificar  la capacidad operativa para la provisión de los servicios de salud.</li>
  </ul>
  <ul>
    <li>MANTENIMIENTO CORRECTIVO. Es la actividad de corrección de las fallas que se presentan de  manera imprevista y que  obligan a  detener el equipo y el servicio que presta.</li>
  </ul>
  <ul>
    <li>MANTENIMIENTO PREVENTIVO. Es la  actividad técnica que  se realiza para asegurar el correcto funcionamiento de los equipos. Implica  realizar periódicamente inspecciones, calibración, reemplazo de componentes,  evaluaciones, etc. a fin de prevenir fallas.</li>
  </ul>
  <p>&nbsp;</p>
  <p><strong>REPOSICIÓN</strong></p>
  <p>Reemplazo de un activo cuyo estado de  conservación es deficiente,  ha superado  su periodo de vida útil, es obsolescente o ha sufrido daños por factores  imprevisibles o por condiciones de seguridad, que afectan la continuidad de sus  operaciones.  </p>
